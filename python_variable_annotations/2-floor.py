#!/usr/bin/env python3
"""2-floor module
This module contains a function that returns the floor of a float number.
"""
import math


def floor(n: float) -> int:
    """Returns the floor of a float number as an integer.

    Args:
        n (float): The float number.
    Returns:
        int: The floor of the float number n.
    """
    return math.floor(n)
