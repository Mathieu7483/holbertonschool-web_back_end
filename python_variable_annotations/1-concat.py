#!/usr/bin/env python3
"""1-concat module
This module contains a function that concatenates two strings.
"""


def concat(x: str, y: str) -> str:
    """Concatenates two strings and returns the result as a string.

    Args:
        x (str): The first string.
        y (str): The second string.

    Returns:
        str: The concatenation of x and y.
    """
    return x + y
