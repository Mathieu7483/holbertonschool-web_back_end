#!/usr/bin/env python3
"""0-add module
This module contains a function that adds two integers.
"""


def add(a: int, b: int) -> int:
    """Adds two integers.

    Args:
        a (int): The first integer.
        b (int): The second integer.

    Returns:
        int: The sum of the two integers.
    """
    return a + b
